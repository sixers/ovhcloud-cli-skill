def quote_yaml(value: str) -> str:
    value = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{value}"'


def display_name(name: str) -> str:
    special = {
        "ovhcloud": "OVHcloud",
        "okms": "OKMS",
        "xdsl": "XDSL",
        "ldp": "LDP",
        "iam": "IAM",
        "vps": "VPS",
        "cdn": "CDN",
        "dns": "DNS",
        "api": "API",
        "ip": "IP",
        "ssh": "SSH",
        "sql": "SQL",
    }
    words = []
    for word in name.split("-"):
        words.append(special.get(word, word.capitalize()))
    return " ".join(words)


def short_description(text: str) -> str:
    text = re.sub(r"\s+", " ", text).strip()
    if len(text) > 64:
        text = text[:61].rstrip() + "..."
    if len(text) < 25:
        text = (text + " workflows").strip()
    return text


def default_prompt(name: str, summary: str) -> str:
    phrase = summary[0].lower() + summary[1:] if summary else "inspect OVHcloud resources safely."
    return f"Use ${name} to {phrase}"


def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n")


def parse_link_index(doc_text: str) -> list[tuple[str, str, str]]:
    matches = re.findall(r"\* \[(ovhcloud [^\]]+)\]\(([^)]+)\)\s*-\s*(.+)", doc_text)
    return [(label.strip(), file_name.strip(), desc.strip()) for label, file_name, desc in matches]


def extract_summary(doc_text: str) -> str:
    lines = [line.rstrip() for line in doc_text.splitlines()]
    seen_title = False
    for line in lines:
        if line.startswith("## "):
            seen_title = True
            continue
        if seen_title and line.strip():
            return line.strip()
    return ""


def fallback_doc_entry(source_repo: Path, doc_name: str, default_summary: str) -> tuple[str, str]:
    doc_path = source_repo / "doc" / doc_name
    if not doc_path.exists():
        return doc_name, default_summary
    return doc_name, extract_summary(doc_path.read_text()) or default_summary


def doc_command_from_path(doc_name: str) -> str:
    stem = Path(doc_name).stem
    return stem[len("ovhcloud_") :].replace("_", " ")


def classify_command(command: str) -> str:
    tokens = command.split()
    last = tokens[-1]
    if last in UNSAFE_TOKENS:
        return "unsafe"
    if last in SAFE_TOKENS:
        return "safe"
    if any(token in UNSAFE_TOKENS for token in tokens):
        return "unsafe"
    if any(token in SAFE_TOKENS for token in tokens):
        return "safe"
    return "neutral"


def select_examples(commands: list[str], kind: str, limit: int = 8) -> list[str]:
    selected = [cmd for cmd in commands if classify_command(cmd) == kind]
    return selected[:limit]


def rel_link(from_file: Path, to_file: Path) -> str:
    return Path(shutil.os.path.relpath(to_file, from_file.parent)).as_posix()


def source_doc_paths(source_repo: Path, prefix: str) -> list[Path]:
    return sorted((source_repo / "doc").glob(f"{prefix}*.md"))


