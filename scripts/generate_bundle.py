def build_generated_content(repo_root: Path, source_repo: Path) -> None:
    bundle_dir = repo_root / "skills" / "ovhcloud"
    if bundle_dir.exists():
        shutil.rmtree(bundle_dir)
    (repo_root / "docs").mkdir(parents=True, exist_ok=True)
    (repo_root / "skills").mkdir(parents=True, exist_ok=True)

    skill_specs = build_skill_specs(source_repo)
    recipe_specs = build_recipe_specs()

    bundle_subskills = skill_specs
    top_doc_links = parse_link_index((source_repo / "doc" / "ovhcloud.md").read_text())
    cloud_doc_links = parse_link_index((source_repo / "doc" / "ovhcloud_cloud.md").read_text())
    link_lookup = {Path(file_name).stem: parse_link_index((source_repo / "doc" / file_name).read_text()) for _, file_name, _ in top_doc_links + cloud_doc_links}

    core_refs = build_core_references(source_repo)
    for name, content in core_refs.items():
        write(bundle_dir / "references" / "generated" / f"{name}.md", content)

    for spec in skill_specs:
        if spec.key == "shared":
            skill_md = build_shared_skill(bundle_dir)
            skill_dir = bundle_dir / "subskills" / spec.key
        elif spec.key == "auth":
            skill_md = build_auth_skill(bundle_dir)
            skill_dir = bundle_dir / "subskills" / spec.key
        elif spec.key == "config":
            skill_md = build_config_skill(bundle_dir)
            skill_dir = bundle_dir / "subskills" / spec.key
        else:
            skill_dir = bundle_dir / "subskills" / spec.key
            skill_md = build_skill_markdown(spec, bundle_dir, skill_dir)
            doc_prefix = spec.doc_prefix or "ovhcloud"
            child_items = link_lookup.get(doc_prefix, [])
            commands = [doc_command_from_path(path.name) for path in source_doc_paths(source_repo, doc_prefix)]
            write(
                bundle_dir / "references" / "generated" / f"{spec.reference_slug}.md",
                build_reference_markdown(spec, repo_root, source_repo, commands, child_items),
            )
        write(skill_dir / "SKILL.md", skill_md)
        write(skill_dir / "agents" / "openai.yaml", "\n".join([
            "interface:",
            f"  display_name: {quote_yaml(display_name(spec.name))}",
            f"  short_description: {quote_yaml(short_description(spec.summary))}",
            f"  brand_color: {quote_yaml(OVH_BRAND_COLOR)}",
            f"  default_prompt: {quote_yaml(default_prompt(spec.name, spec.summary))}",
        ]))

    for recipe in recipe_specs:
        recipe_dir = bundle_dir / "recipes" / recipe.key
        write(recipe_dir / "SKILL.md", build_recipe_markdown(bundle_dir, recipe))
        write(recipe_dir / "agents" / "openai.yaml", "\n".join([
            "interface:",
            f"  display_name: {quote_yaml(display_name(recipe.name))}",
            f"  short_description: {quote_yaml(short_description(recipe.summary))}",
            f"  brand_color: {quote_yaml(OVH_BRAND_COLOR)}",
            f"  default_prompt: {quote_yaml(default_prompt(recipe.name, recipe.summary))}",
        ]))

    bundle_skill = build_bundle_skill(bundle_dir, bundle_subskills, recipe_specs)
    write(bundle_dir / "SKILL.md", bundle_skill)
    write(bundle_dir / "agents" / "openai.yaml", "\n".join([
        "interface:",
        f"  display_name: {quote_yaml('OVHcloud')}",
        f"  short_description: {quote_yaml('OVHcloud CLI bundle and workflows')}",
        f"  brand_color: {quote_yaml(OVH_BRAND_COLOR)}",
        f"  default_prompt: {quote_yaml('Use $ovhcloud to choose the right OVHcloud CLI subskill and stay read-only by default.')}",
    ]))

    write(bundle_dir / "references" / "skills-index.md", build_local_skills_index(skill_specs, recipe_specs))
    write(bundle_dir / "recipes" / "index.md", build_recipe_index(recipe_specs))
    write(repo_root / "docs" / "skills.md", build_skills_index(skill_specs, recipe_specs))


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate the OVHcloud skill bundle from an ovhcloud-cli clone.")
    parser.add_argument(
        "--source-repo",
        default="../ovhcloud-cli",
        help="Path to the source ovhcloud-cli repository clone.",
    )
    parser.add_argument(
        "--repo-root",
        default=".",
        help="Path to the skill bundle repository root.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    repo_root = Path(args.repo_root).resolve()
    source_repo = Path(args.source_repo).resolve()
    if not (source_repo / "doc" / "ovhcloud.md").exists():
        raise SystemExit(f"Could not find source docs under {source_repo}")
    build_generated_content(repo_root, source_repo)
    print(f"Generated OVHcloud skill bundle under {repo_root}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
